public class Point {
	
	private String x;

	private String y;

	public void setX(String x) {
		this.x = x;
	}

	public String getX() {
		return this.x;
	}

	public void setY(String y) {
		this.y = y;
	}

	public String getY() {
		return this.y;
	}

}